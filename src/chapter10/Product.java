package chapter10;

public class Product
{
	private String name;
	private float price;
	private int count;

	// name属性的getter方法
	public String getName()
	{
		return name;
	}

	// name属性的setter方法
	public void setName(String name)
	{
		this.name = name;
	}

	// price属性的getter方法
	public float getPrice()
	{
		return price;
	}

	// price属性的setter方法
	public void setPrice(float price)
	{
		this.price = price;
	}

	// count属性的getter方法
	public int getCount()
	{
		return count;
	}

	// count属性的setter方法
	public void setCount(int count)
	{
		this.count = count;
	}
}
