package chapter14;

public class Product
{
	private String name;
	private float price;
	// name属性的getter方法
	public String getName()
	{
		return name;
	}
	// name属性的setter方法
	public void setName(String name)
	{
		this.name = name;
	}
	// price属性的getter方法
	public float getPrice()
	{
		return price;
	}
	// price属性的setter方法
	public void setPrice(float price)
	{
		this.price = price;
	}
}
