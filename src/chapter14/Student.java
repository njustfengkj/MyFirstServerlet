package chapter14;

public class Student
{
    private String name;
    private int grade;
	// name属性的getter方法
	public String getName()
	{
		return name;
	}
	// name属性的setter方法
	public void setName(String name)
	{
		this.name = name;
	}
	// grade属性的getter方法
	public int getGrade()
	{
		return grade;
	}
	// grade属性的setter方法
	public void setGrade(int grade)
	{
		this.grade = grade;
	}
}
